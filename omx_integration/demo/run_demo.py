def check_redis_available(host: str = "localhost", port: int = 6379) -> bool:
    """Check if Redis is available."""
    try:
        import redis
        r = redis.Redis(host=host, port=port, socket_connect_timeout=2)
        r.ping()
        return True
    except Exception:
        return False


