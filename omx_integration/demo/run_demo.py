def run_demo_without_redis():
    """
    Run the demo without Redis — writes files directly.
    Fallback mode for environments where Redis is not available.
    """
    project_dir = PROJECT_DIR

    print("=" * 60)
    print("  oh-my-codex Integration Demo (No-Redis Fallback)")
    print("  VaultWares Agentciation × OMX Team Orchestration")
    print("=" * 60)
    print(f"\nProject directory: {project_dir}")
    print("Redis: NOT AVAILABLE — running in file-only mode")
    print()

    tasks = get_demo_tasks()
    all_files = []

    for i, task_spec in enumerate(tasks):
        worker_id = f"worker-{i+1}"
        print(f"\n[{worker_id}] Executing: {task_spec['subject']}")

        for rel_path, content in task_spec.get("output_files", {}).items():
            abs_path = os.path.join(project_dir, rel_path)
            os.makedirs(os.path.dirname(abs_path), exist_ok=True)
            with open(abs_path, "w", encoding="utf-8") as f:
                f.write(content)
            all_files.append(rel_path)
            print(f"  [{worker_id}] Wrote: {rel_path}")

    # Git commit all files
    if all_files:
        try:
            subprocess.run(
                ["git", "add"] + all_files,
                cwd=project_dir,
                capture_output=True,
                text=True,
                check=True,
            )
            subprocess.run(
                ["git", "commit", "-m",
                 "OMX Demo: agentic team code generation (no-Redis fallback)\n\n"
                 f"Files: {', '.join(all_files)}\n\n"
                 "Confidence: high\n"
                 "Scope-risk: narrow\n"
                 "Tested: file creation verified"],
                cwd=project_dir,
                capture_output=True,
                text=True,
                check=True,
            )
            print(f"\nCommitted {len(all_files)} files to git.")
        except subprocess.CalledProcessError as e:
            print(f"Git note: {e.stderr.strip() if e.stderr else 'already committed'}")

    print("\n" + "=" * 60)
    print("  Demo Results (No-Redis Fallback)")
    print("=" * 60)
    print(f"  Files written: {len(all_files)}")
    print(f"  All committed to current branch")
    print("=" * 60)


